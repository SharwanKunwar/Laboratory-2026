package Project.LibraryManagementSystem;

public class Books {
    String name;
    String author;
    int pages;
    int price;
    String category;

    public Books(String name, String author, int pages, int price, String category) {
        this.name = name;
        this.author = author;
        this.pages = pages;
        this.price = price;
        this.category = category;
    }
}
